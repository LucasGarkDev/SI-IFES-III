    private void salvarAlteracoes() {
        if (linhaEditando == -1) {
            mostrarMensagem("Nenhuma edição em andamento!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Obtendo os dados dos campos
            String origem = txtOrigem.getText().trim();
            String raca = cbxRaca.getSelectedItem().toString();
            Date dataNascimento = dateBovino.getDate();
            double peso = Double.parseDouble(formatedPesoBovino.getText().trim());
            String status = cbxStatus.getSelectedItem().toString();
            String fazenda = cbxFazendaBovino.getSelectedItem().toString();
            String vacina = chxVacinado.isSelected() ? cbxVacina.getSelectedItem().toString() : "Nenhuma";

            // Validando campos obrigatórios
            if (origem.isEmpty() || dataNascimento == null || formatedPesoBovino.getText().trim().isEmpty()) {
                mostrarMensagem("Preencha todos os campos obrigatórios!", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Atualizando o objeto Bovino na lista
            Bovino bovinoEditado = tableModel.getLista().get(linhaEditando);
            bovinoEditado.setOrigem(origem);
            bovinoEditado.setRaca(raca);
            bovinoEditado.setDataNascimento(dataNascimento);
            bovinoEditado.setPeso(peso);
            bovinoEditado.setStatus(status);
            bovinoEditado.setFazenda(fazenda);
            bovinoEditado.setVacina(vacina);

            // Notifica a tabela que os dados foram alterados
            tableModel.fireTableRowsUpdated(linhaEditando, linhaEditando);

            linhaEditando = -1; // Resetando o índice de edição
            limparCampos();
            mostrarMensagem("Alteração realizada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

        } catch (NumberFormatException e) {
            mostrarMensagem("O campo 'Peso Atual' deve ser um número válido!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }


    // Método para carregar os dados da tabela para os campos
    private void carregarDadosParaCampos() {
        int linhaSelecionada = tabelaBovinos.getSelectedRow();

        if (linhaSelecionada == -1) {
            mostrarMensagem("Selecione um bovino para alterar!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Pegamos o objeto Bovino da lista do modelo
        Bovino bovinoSelecionado = tableModel.getLista().get(linhaSelecionada);

        // Preenchendo os campos do formulário com os dados do bovino selecionado
        txtOrigem.setText(bovinoSelecionado.getOrigem());
        cbxRaca.setSelectedItem(bovinoSelecionado.getRaca());
        dateBovino.setDate(bovinoSelecionado.getDataNascimento()); // Setando a data no JDateChooser
        formatedPesoBovino.setText(String.valueOf(bovinoSelecionado.getPeso()));
        cbxStatus.setSelectedItem(bovinoSelecionado.getStatus());
        cbxFazendaBovino.setSelectedItem(bovinoSelecionado.getFazenda());

        // Configuração do checkbox e combobox de vacina
        boolean vacinado = !bovinoSelecionado.getVacina().equals("Nenhuma");
        chxVacinado.setSelected(vacinado);
        cbxVacina.setEnabled(vacinado);
        cbxVacina.setSelectedItem(bovinoSelecionado.getVacina());

        // Guardamos a linha que está sendo editada
        linhaEditando = linhaSelecionada;
    }


    private void inserirBovino() {
        try {
            // Obtendo os dados dos campos
            String origem = txtOrigem.getText().trim();
            String raca = cbxRaca.getSelectedItem().toString();
            Date dataNascimento = dateBovino.getDate(); // Pega a data diretamente do JDateChooser
            double peso = Double.parseDouble(formatedPesoBovino.getText().trim());
            String status = cbxStatus.getSelectedItem().toString();
            String fazenda = cbxFazendaBovino.getSelectedItem().toString();
            String vacina = chxVacinado.isSelected() ? cbxVacina.getSelectedItem().toString() : "Nenhuma";

            // Validando campos obrigatórios
            if (origem.isEmpty() || dataNascimento == null || formatedPesoBovino.getText().trim().isEmpty()) {
                mostrarMensagem("Preencha todos os campos obrigatórios!", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Criando um novo objeto Bovino
            Bovino novoBovino = new Bovino(contadorID++, origem, raca, dataNascimento, peso, status, fazenda, vacina);

            // Adicionando ao modelo da tabela
            tableModel.adicionar(novoBovino);

            // Limpando os campos
            limparCampos();

            mostrarMensagem("Bovino cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

        } catch (NumberFormatException e) {
            mostrarMensagem("O campo 'Peso Atual' deve ser um número válido!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void excluirBovino() {
        int linhaSelecionada = tabelaBovinos.getSelectedRow();

        if (linhaSelecionada == -1) {
            mostrarMensagem("Selecione um bovino para excluir!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este bovino?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if (confirmacao == JOptionPane.NO_OPTION) return;

        // Removendo o objeto da lista do modelo
        tableModel.remover(linhaSelecionada);

        mostrarMensagem("Bovino excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    }

    
    // Método para limpar os campos após inserção ou edição
    private void limparCampos() {
        txtOrigem.setText("");
        cbxRaca.setSelectedIndex(0);
        dateBovino.setDate(null);
        formatedPesoBovino.setText("");
        cbxStatus.setSelectedIndex(0);
        cbxFazendaBovino.setSelectedIndex(0);
        chxVacinado.setSelected(false);
        cbxVacina.setEnabled(false);
        cbxVacina.setSelectedIndex(0);
        txtOrigem.requestFocus();
    }

